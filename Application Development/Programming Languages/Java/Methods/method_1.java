package JAVA.Methods;

public class method_1 {
        // Methods are actually functions which are known as methods in java
    static void fun1(){
        System.out.println("This is the first function of our code");
    }

    static int fun2(){
        return 2;
    }
    public static void main(String[] args) {
        fun1();
        System.out.println(fun2());
    }
}